Modèle-Vue-Contrôleur: une méthode de conception de logiciel. 
Modèle d'architecture qui cherche à séparer nettement les couches de présentation (UI : User Interface),
 métier (BLL : Business Logic Layer) et d'accès aux données (DAL : Data Access Layer).
  Le but étant d'avoir une dépendance minimale entre les différentes couches de l'application ;
   ainsi les modifications effectuées sur n'importe quelle couche de l’application n'affectent pas les autres couches.